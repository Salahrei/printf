#include "ft_printf.h"

void	ft_print_HEXA(unsigned int nb)
{
	char	c;
	if(nb >= 16)
	{
		ft_print_HEXA(nb / 16);
	}
	c = "0123456789ABCDEF"[nb % 16];
	write(1, &c, 1);
}
